/// A method is a procedure that does some computation.
void greet(String name) {
    IO.println("Hello, " + name + "!");
}

/// A function is a method that takes one or more inputs
/// and produces a single output.
int add(int a, int b) {
    return a + b;
}

int sub(int a, int b) {
    return a - b;
}

int negate(int n) {
    return -n;
}
